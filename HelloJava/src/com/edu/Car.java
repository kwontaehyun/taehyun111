package com.edu;

public class Car {
	private String model;
	private int price;
	
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getModel() {
		return model;
	}
	public String setModel()
	{
		return this.model;
	}
	public void setModel(String model)
	{
		this.model = model;
	}
}
